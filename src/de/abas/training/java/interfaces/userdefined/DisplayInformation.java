package de.abas.training.java.interfaces.userdefined;

/**
 * User defined interface for displaying information.
 *
 * @author abas Software AG
 *
 */
public interface DisplayInformation {
	/**
	 * Method stub to displays detailed information.
	 */
	abstract void displayInformationDetailed();

	/**
	 * Method stub to displays short information.
	 */
	abstract void displayInformationShort();
}
